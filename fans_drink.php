<h1>&gt; &gt; the o.c. drinking game</h1>

<p>Yeah, everyone seems to have their own version of an O.C. drinking game, but all the cool people (read: my friends and I) are playing this one. So get yourself some beers or margaritas (or orange soda for all you underage fans) and get your drink on. One drink is equal to a normal sip, but feel free to drink more if you want to get really wasted. We don't really condone teen drinking (you don't want to end up smelling like Marissa!), but come on, everyone in The O.C. is a closet alcoholic!</p>
 
<p><strong>Take a drink everytime:</strong></p>

<p><strong>Someone mentions the first season.</strong> Yep, we remember the way it was before. Each time you catch a reference (which probably means that you watch too much O.C.) it means you have to drink to the good old days.</p>

<p><strong>Marissa wears clothes that don't match.</strong> Maybe it's cool to mix pink stripes with a brown pattern in the O.C., but the rest of us have to be buzzing to understand the fashion of this one.</p>

<p><strong>Someone almost dies.</strong> Whether it's Julie pointing a gun at her pornographer ex or Kirsten being pummled by a truck, near-death seems to come to the O.C. often. Finish the beer if someone actually dies.</p>

<p><strong>There's a fight.</strong> The O.C. wouldn't be the same without all the punches that are being thrown. This is defnitely an excuse to slam an empty beer bottle on the table and say &quot;Welcome to the O.C., bitch!&quot;</p>

<p><strong>The kids actually go to school.</strong> When I was in high school, I saw that terrible building every day of my life, and it wasn't half as nice as Harbor High.</p>

<p><strong>Finish your drink if:</strong></p>

<p><strong>Summer dresses up in a faux leather outfit.</strong> I don't think I have to remind you about the Wonder Woman outfit, the outfit from the drawing lesson, and the Little Miss Vixen getup. When Summer dresses up as her far kinkier alter ego, it's definitely an excuse to drink up!</p>

<p><strong>An old character comes back.</strong> Because, like Seth so eloquently put it, &quot;No one ever leaves and comes back!&quot;

<p><strong>Seth and Summer or Ryan and Marissa have a break-up or make-up moment.</strong> Because it would be completely out of character for them to go three episodes as a stable couple, or un-couple.</p>

