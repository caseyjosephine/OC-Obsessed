<h1>&gt; &gt; the oc facts</h1>

<p>Want some neat O.C. trivia to impress your friends? Here are some OC facts courtesy of the <a href="http://www.imdb.com">internet movie database.</a></p>

<p>Due to labor union salary rules about filming outside of Los Angeles County, the show is actually filmed at Raleigh Studios in Manhattan Beach (40 miles away from the actual Newport Beach) to reduce filming costs.</p>

<p>The Cohen's pool is only four feet deep, so the cast do the pool scenes on their knees.</p>

<p>In &quot;The L.A.&quot; Seth makes a comment about self indulgent actors with instruments. Adam Brody has his own band called Big Japan.</p>

<p>The O.C. was originally intended to be called &quot;Orange County,&quot; but a movie of the same name had recently come out.</p>

<p>Seth Cohen (Adam Brody) says in an episode &quot;I'm not reading that, that's like The Ring. I don't wanna die.&quot; Adam Brody appears in The Ring (2002).</p>

<p>In episode #2.24, Jimmy Cooper (Tate Donovan) is talking to Hailey Nichol (Amanda Righetti). She asks him how it is living in Hawaii, and he says it's great, and that she &quot;wouldn't believe the North Shore.&quot; Hailey replies, &quot;So I've heard.&quot; This is a reference to the series &quot;North Shore&quot; (2004), in which Amanda Righetti starred.</p>

<p>In episode 16 of the first season, some of the characters are apparently watching an Enrique Iglesias music video on television. While we can't see the TV, we can hear it- the video is &quot;Addicted&quot; featuring cast member Mischa Barton (Marissa)</p>

<p>The character 'Sandy Cohen' was ranked #25 in TV Guide's list of the &quot;50 Greatest TV Dads of All Time&quot; (20 June 2004 issue).</p>

<p>Adam Brody originally auditioned for the part of Ryan Atwood (Benjamin McKenzie).</p>

<p>The entire cast (including newcomer Michael Cassidy, creator Josh Schwartz, and producer McG) was given the key to the city of Newport Beach, California by Mayor Tod Ridgeway on October 28, 2004 at The Historic Balboa Pavilion in Newport Beach.</p>

<p>In an early draft of the series, Ryan Atwood was going to be Sandy Cohen's illegitimate child.</p>

<p>Seth's middle name is Ezekiel.</p>

<p>Adam Brody (Seth Cohen) doesn't like boats, so when he sails away in episode 1.27 &quot;The Tie That Binds&quot; he is not in the boat at all. It is a 40-year old stunt double in a wig that is actually sailing the boat.</p>

<p>In one of the first season episodes (&quot;The Proposal&quot;), the main characters are watching an unnamed &quot;chick-flick.&quot; According to &quot;The O.C.&quot; website, the movie was &quot;The Triplets of Bellville,&quot; but a closer examination will show that the credits are not from the movie at all. Instead, the credits being run in the theater in the background are just shout-outs to crew members (i.e. Susan Godfrey).</p>

<p>In Episode 7, Season 2, Seth Cohen asks Ryan if he &quot;puked like that girl from Sixth Sense, or like that fat guy in Monty Python.&quot; Mischa Barton (Marissa) played the girl who vomited in the The Sixth Sense (1999).</p>

<p>The character of Renee Wheeler (played by Kathleen York) is named for the Wheeler School in Providence, Rhode Island, series creator Josh Schwartz's high school.</p>

<p>Episode #1.12 opens with Marissa and Ryan talking in class. In the background you can hear the teacher calling down students. If you listen closely you can hear her call out Stephanie Savage, who is not a student at Newport but one of the producers.</p>

